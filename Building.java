package com;
class Building{
private  double height, width;
private  int number_of_rooms;//instance variables
private String owner;

void setHeight(double height){
this.height=height;
}

double getHeight(){
return height;
}

void setWidth(double width){
this.width=width;
}

double getWidth(){
return width;
}

void setNumber_of_rooms(int number_of_rooms){
this.number_of_rooms=number_of_rooms;
}

int getNumber_of_rooms(){
return number_of_rooms;
}

void setowner(String owner){
this.owner=owner;
}

String getowner(){
return owner;
}
}

